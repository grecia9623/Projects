primer_numero = int(input("Ingresa un numero (1):"))
segundo_numero = int(input("Ingresa un numero (2):"))
#operaciones aritmeticas
print(f'La suma es: {primer_numero + segundo_numero}')
print(f'La multiplicacion es: {primer_numero * segundo_numero}')
#operaciones de comparacion
print(f'¿Los numeros son iguales? {primer_numero == segundo_numero}')
print(f'¿El primer numero es menor que el segundo? {primer_numero < segundo_numero}')
print(f'¿el segundo numero es mayor o igual al primero? {segundo_numero >= primer_numero}')


